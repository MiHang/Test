package pers.test.model;

/**
 * 优秀学生名单
 */
public class ExcellentStudentNameList {

    private String name; // 姓名
    private String classes; // 班级
    private String branchCollege; // 分院
    private String major; // 专业

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getClasses() {
        return classes;
    }

    public void setClasses(String classes) {
        this.classes = classes;
    }

    public String getBranchCollege() {
        return branchCollege;
    }

    public void setBranchCollege(String branchCollege) {
        this.branchCollege = branchCollege;
    }

    public String getMajor() {
        return major;
    }

    public void setMajor(String major) {
        this.major = major;
    }
}
